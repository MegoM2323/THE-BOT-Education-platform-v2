-- Добавляем CHECK constraint для максимального баланса кредитов
-- Максимальный баланс = 10000 кредитов (бизнес-логика для предотвращения накопления)
-- Гарантирует что баланс никогда не превысит максимальный лимит на уровне БД

ALTER TABLE credits ADD CONSTRAINT check_balance_max CHECK (balance <= 10000);
